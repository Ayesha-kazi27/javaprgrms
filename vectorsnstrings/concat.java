import java.util.*;
public class concat {
    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);
        System.out.println("Enter the 1st String");
        String a = sc.next();
        System.out.println("Enter the 2nd String");
        String b = sc.next();
        System.out.println("The concatenated string is : "+a+" "+b);
        sc.close();
    }
}